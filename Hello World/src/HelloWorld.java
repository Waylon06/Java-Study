//public 表示公开的
//class 表示定义一个类
//HelloWorld 表示一个类名
public class HelloWorld {    //表示一个定义公开的类名，起名HelloWorld
    /*
    static 表示静态的
    void 空
    main表示方法名是main
    String[]是一个main方法的形式参数列表
    */
    public static void main(String[] args){       //表示定义一个公开的主方法
        System.out.println("Hello World!");
        System.out.println("Waylon is a handsome boy!");
        System.out.println("401的都是我好大儿!");
    }
}
